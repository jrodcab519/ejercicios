

public class Bucle3 {

    public static void main(String[] args) {

        for(int i = 0; i <10; i++){
            System.out.println(i);
        }

        System.out.println("Fin del bucle for");

        int j = 0;
        while(j < 10){
            System.out.println(j);
            j++;
        }

        System.out.println("Fin del bucle while");

    }
}
